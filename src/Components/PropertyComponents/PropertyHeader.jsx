export default function ComplexNavbar() {

    const [open, setOpen] = React.useState(false);
  const openDrawer = () => setOpen(true);
  const closeDrawer = () => setOpen(false);
  const [isNavOpen, setIsNavOpen] = React.useState(false);
 
  const toggleIsNavOpen = () => setIsNavOpen((cur) => !cur);
 
  React.useEffect(() => {
    window.addEventListener(
      "resize",
      () => window.innerWidth >= 960 && setIsNavOpen(false),
    );
  }, []);


  const params        = useParams();
  const dispatch      = useDispatch();
  const { id, token } = params;
  const { name }      = useSelector((state) => state.user)

  useEffect(() => {
    const verifyEmailUrl = async () => {
      try {
        if (id && token) {
          const response = await EmailVerify(params.id, params.token);
          console.log("iam the response ",response);
          if (response.status === 200) {
            localStorage.setItem("propertyToken", response.data.propertyToken);
          
            setTimeout(() => {
              GenerateSuccess(response.data.message);
            }, 300);
          
            dispatch(
                setPropOwnerDetails({
                id: response.data.userData._id,  
                name: response.data.userData.name,
                email: response.data.userData.email,
                mobile: response.data.userData.mobile,
                image : response.data.userData.image,
              })
            );
          }
          
        }
      } catch (error) {
        console.error(error);
      }
    };
  
    verifyEmailUrl(); 
  
    
  }, [id, token, params.id, params.token]);
  
  return (
    <>
    <Drawer open={open} onClose={closeDrawer}>
        <div className="mb-2 flex items-center justify-between p-4">
          <Typography variant="h5" color="blue-gray">
            Side Menu
          </Typography>
          <IconButton variant="text" color="blue-gray" onClick={closeDrawer}>
            <svg
              xmlns="http://www.w3.org/2000/svg"
              fill="none"
              viewBox="0 0 24 24"
              strokeWidth={2}
              stroke="currentColor"
              className="h-5 w-5"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                d="M6 18L18 6M6 6l12 12"
              />
            </svg>
          </IconButton>
        </div>
        <DefaultSidebar closeSidebar={closeDrawer} />
      </Drawer>
    <Navbar className="w-full p-2 h-max max-w-full mr-4">
        <div className="flex items-center justify-between text-blue-gray-900">
    <div className="flex align-middle justify-between">
            <svg
              onClick={openDrawer}
              xmlns="http://www.w3.org/2000/svg"
              fill="none"
              viewBox="0 0 24 24"
              stroke="currentColor"
              className="h-9 w-9 lg:h-12  lg:pr-2"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth="2"
                d="M4 6h16M4 12h16M4 18h16"
              />
            </svg>
          
          </div>
       
  <div className="relative flex items-center text-blue-gray-900">
        
        <IconButton
          size="sm"
          color="blue-gray"
          variant="text"
          onClick={toggleIsNavOpen}
          className="ml-auto mr-2 lg:hidden"
        >
          <Bars2Icon className="h-6 w-6" />
        </IconButton>
        
        <Button size="sm" variant="text">
          <span>{name}</span>
        </Button>
        <div className="mr-4">
        <ProfileMenu />
        </div>
    
      </div>
      </div>
      <MobileNav open={isNavOpen} className="overflow-scroll">
        <NavList />
      </MobileNav>
    </Navbar>
    </>
  );
}